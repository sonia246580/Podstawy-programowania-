# String manipulation

movie = "The Lord of the Rings: The Return of the King"

# Number of characters
print('Number of characters:', len(movie))

# Title in uppercase
print(movie.upper())

# Title in lowercase
print(movie.lower())

# How many times the letter "e" appears in the title
print(movie.count('e'))

# Where in the text is the word "Lord"
print(movie.find('Lord'))

# Where in the text is the word "dragon"
print(movie.find('dragon'))  

