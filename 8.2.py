# Calculation of circle area and circumference 
#

# determine radius and PI values
# calculate area 
# calculate circumference 
# print results
 
r = float(input("Enter radius: "))
pi = 3.14

area = pi * r * r

circumference = 2 * pi * r

print(f'circumference = {circumference}')
print(f'area = {area}')
